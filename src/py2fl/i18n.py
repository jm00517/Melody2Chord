def t(key: str, lang: str = DEFAULT_LANG) -> str:
    """Translate a key, falling back to English then to the key itself."""
    lang_dict = STRINGS.get(lang) or STRINGS[DEFAULT_LANG]
    if key in lang_dict:
        return lang_dict[key]
    return STRINGS[DEFAULT_LANG].get(key, key)


def lang_from_environ(environ: dict | None) -> str:
    if not environ:
        return DEFAULT_LANG
    cookie_header = environ.get("HTTP_COOKIE") or ""
    if cookie_header:
        cookie = SimpleCookie()
        try:
            cookie.load(cookie_header)
        except Exception:
            cookie = SimpleCookie()
        morsel = cookie.get(COOKIE_NAME)
        if morsel and morsel.value in SUPPORTED_LANGS:
            return morsel.value
    from urllib.parse import parse_qs
    query = parse_qs(environ.get("QUERY_STRING", ""))
    candidate = (query.get("lang") or [DEFAULT_LANG])[0]
    if candidate in SUPPORTED_LANGS:
        return candidate
    return DEFAULT_LANG


def normalize_lang(value: str | None) -> str:
    if value in SUPPORTED_LANGS:
        return value
    return DEFAULT_LANG
